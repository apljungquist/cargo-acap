impl<'a> ser::SerializeStruct for &'a mut Serializer {
    type Ok = ();
    type Error = Err;

    fn serialize_field<T: ?Sized>(
        &mut self,
        key: &'static str,
        value: &T,
    ) -> Result<(), Self::Error>
    where
        T: Serialize,
    {
        self.output += key;
        self.output.push('=');
        value.serialize(&mut **self)?;
        self.output.push('\n');
        Ok(())
    }

    fn end(self) -> Result<Self::Ok, Self::Error> {
        Ok(())
    }
}

